import { clientCredentials } from '../utils/client';

const endpoint = clientCredentials.databaseURL;

const getFolders = () => new Promise((resolve, reject) => {
  fetch(`${endpoint}/folders.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});
const createFolder = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/folders.json`, {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then((data) => resolve(data))
    .catch(reject);
});
const updateFolder = (payload) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/folders/${payload.firebaseKey}.json`, {
    method: 'PATCH',
    headers: {
      'Content-Type': 'application/json',
    },
    body: JSON.stringify(payload),
  })
    .then((response) => response.json())
    .then(resolve)
    .catch(reject);
});
const getUserFolders = (uid) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/Folders.json?orderBy="uid"&equalTo="${uid}"`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => {
      if (data) {
        resolve(Object.values(data));
      } else {
        resolve([]);
      }
    })
    .catch(reject);
});
const getSingleFolder = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/folders/${firebaseKey}.json`, {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve((data)))
    .catch(reject);
});
const deleteFolder = (firebaseKey) => new Promise((resolve, reject) => {
  fetch(`${endpoint}/folders/${firebaseKey}.json`, {
    method: 'DELETE',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then((response) => response.json())
    .then((data) => resolve((data)))
    .catch(reject);
});

export {
  getFolders,
  createFolder,
  updateFolder,
  getUserFolders,
  getSingleFolder,
  deleteFolder,

};
